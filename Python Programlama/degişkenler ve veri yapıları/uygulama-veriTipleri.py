# Yarıçapı verilen bir dairenin alanını ve çevresini hesaplama
yaricap = float(input("Yariçapi giriniz: "))
pi = 3.14
daireninAlani = pi * yaricap ** 2
daireninCevresi = 2 * pi * yaricap

print("Dairenin Alani:", daireninAlani)
print("Dairenin Çevresi:", daireninCevresi)

# Klavyeden girilen km bilgisini mil cinsinden hesaplayınız
km = float(input("Lütfen km giriniz: "))
mil = km / 1.609344
print("KM to Mile Conversion:", mil)
